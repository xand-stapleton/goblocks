// Eval returns the m-th derivative of (1 - r^2)^(-1/2)
// at the crossing-symmetric point.
func (p *Phi1Numeric) Eval(i, j int, r, nu float64) (float64, error) {
	if j > 0 {
		return 0.0, nil
	}
	if math.Abs(r-0.1715728753) > float64(1e-9) {
		return 0, fmt.Errorf("Numeric evaluation of phi1 is only implemented near the crossing symmetric point, 3-2sqrt(2), not %g.", r)
	}
	if math.Abs(nu-0.5) > float64(1e-9) {
		return 0, fmt.Errorf("Numeric evaluation of phi1 is only implemented in d=3, not %d.", int(2*nu+2))
	}
	val, ok := p.derivatives[i]
	if !ok {
		return 0, fmt.Errorf("derivative of order %d not found", i)
	}
	return val, nil
}
